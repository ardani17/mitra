<script>
    function openRejectModal(requestId, userName) {
        document.getElementById('rejectUserName').textContent = userName;
        document.getElementById('rejectForm').action = `/telegram-bot/registrations/${requestId}/reject`;
        document.getElementById('rejectModal').classList.remove('hidden');
    }
    
    function closeRejectModal() {
        document.getElementById('rejectModal').classList.add('hidden');
        document.getElementById('reject_reason').value = '';
    }
    
    // Auto-refresh every 30 seconds to check for new registrations
    setTimeout(function() {
        location.reload();
    }, 30000);
    
    // Browser notification for pending requests
    @if($requests->count() > 0)
        if (Notification.permission === "granted") {
            new Notification("Telegram Bot", {
                body: "You have {{ $requests->count() }} pending registration requests",
                icon: "/favicon.ico"
            });
        } else if (Notification.permission !== "denied") {
            Notification.requestPermission().then(function (permission) {
                if (permission === "granted") {
                    new Notification("Telegram Bot", {
                        body: "You have {{ $requests->count() }} pending registration requests",
                        icon: "/favicon.ico"
                    });
                }
            });
        }
    @endif
</script>
